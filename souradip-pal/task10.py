# Import the numpy library and give it the alias 'np'
import numpy as np

# 10. Create a numpy array with strings and specify the data type explicitly

# Create a numpy array 'x' that contains the strings 'Toronto' and 'Montreal', specifying the data type as 'str'.
x = np.array(['Toronto', 'Montreal'], dtype=str)

# Print the content of array 'x', which contains the strings.
print(x)

# Print the data type of the elements in array 'x'.
print(x.dtype)
