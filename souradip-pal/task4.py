# Import the numpy library and give it the alias 'np'
import numpy as np

# 4. Arrange numpy between numbers

# Create a numpy array 'a' using np.arange() that generates numbers from 4 to 11 (inclusive).
a = np.arange(4, 12)

# Print the content of array 'a', which contains the range of numbers between 4 and 11.
print(a)

# Print the shape (dimensions) of array 'a'.
print(a, '.shape : ', a.shape)
